def reverse_string(phrase):
    """Reverse string,

        >>> reverse_string('awesome')
        'emosewa'

        >>> reverse_string('sauce')
        'ecuas'
    """
    return phrase[::-1]

print(f'reverse_string("hello world") = {reverse_string("hello world")}')

print(f'reverse_string("egassem terces") = {reverse_string("egassem terces")}')